<p>Situé dans le XIème arrondissement de Paris, le Bataclan est une salle de spectacle mythique. Il accueille les plus grands noms de la musique et de la comédie au sein d’une activité éclectique laissant place aux spectacles, théâtre, discothèque mais surtout aux concerts. De célèbres artistes, à l’instar de Snoop dogg, Eels, Oasis, The Kills, Stromae, Robbie Williams, IAM, ou encore Damien Saez ont choisi cet endroit devenu unique par son passé pour se représenter, et alimentent jour après jour l’histoire et la magie du Bataclan.</p>
<p style="text-align:center;">50, boulevard Voltaire 75011 Paris</p>
