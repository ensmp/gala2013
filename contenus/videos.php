<p><strong>Cherokee</strong> (Shiny Disco Club)<br/>
Assurément le duo montant Français d’électro, marquant le retour du légendaire style House de la fin des années 90. Après seulement quelques singles, ce groupe fut mondialement reconnu et a eu le privilège d’être booké dans les meilleurs clubs des villes branchées du monde entier (Paris, Montréal, Bruxelles, Santa Monica, Miami...). 
Après avoir cartonné à leur release party au Social Club, ils en sont devenu résidents.
En étant capables de faire reconnaitre leur style dès les premiers beats d’un morceau, Cherokee s’emploie à faire passer à son audience une soirée pleine de plaisir et d’énergie. Vous n’arrêterez pas de danser tant qu'ils n'auront pas fini leur set !</p>
