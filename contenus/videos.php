<p><strong>Beataucue</strong><br/>
Alexis et Médéric forment ensemble Beataucue, un duo électro originaire de Caen. Tout a commencé pour eux en 2009 en réalisant un remix de Major Lazer. Avec ce premier succès ainsi que le soutien de Diplo ils se sont imposés sur la scène électro en produisant beaucoup de titres énergiques et de remixes (notamment de Kylie Minogue, Boys Noize, Mika, Yelle Two Door Cinema Club et Steve Aoki), en signant sur des labels tels que Ultra Records, Universal Music, Ministry Of Sound et EMI.</br>
Connu pour ses remixes explosifs et acclamé par les adeptes d'électro et DJs du monde entier, le jeune duo de français a réussi à imposer son style avec la sortie des ses trois EPs chez Kitsune, dont le dernier, Falcon Punch, à l'été 2011. Ils se sont produits sur scène dans les plus grandes villes telles que Los Angeles, Tokyo, San Francisco, Bruxelles, Barcelone, et bien sûr Paris !
Quand on sait que tout est parti d'un simple jeu entre amis, leur ascension fulgurante est encore plus impressionnante.</p>
