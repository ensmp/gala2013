<h1>Bienvenue sur le site du Gala des Mines de Paris</h1>
<p id="bienvenue"> Bienvenue sur le site du gala des Mines de Paris. Pour trouver les rubriques, cherche à interagir avec les personnages sur les côtés. Mais n'oublie pas de rechercher les zones cachées ! Il te suffit de passer ta souris au dessus de certaines zones et de cliquer sur les objets que tu pourra trouver. Un indice : ton curseur change quand tu passes au dessus d'un objet cliquable. 
</p>

<p>
<strong>Conseil :</strong> pour un fonctionnement optimal, déplace ta souris <strong>doucement</strong>.
</p>
